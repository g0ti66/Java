public class M3M7 {

    public static void main(String[] args) {

        for (int i = 1; i <= 3; i++) {
            for (int j = 0; j <= 9; j++) {
                for (int k = 1; k <= 3; k++) {
                    System.out.print(j);
                }
            }
            System.out.println();
        }
    }
}