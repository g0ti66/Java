package fr.dampierre;

public class M3S1 {

    public static void main(String[] args) {

        int nb = -4;

        // Je veux répéter 6 fois un certain nombre de choses
        for (int i = 1; i <= 6; i++) {
            // On affiche nb
            System.out.print(nb + " ");
            // On ajoute ensuite 18 à nb
            nb += 18;
        }
    }

}
