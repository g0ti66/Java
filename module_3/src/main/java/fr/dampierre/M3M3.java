package fr.dampierre;

public class M3M3 {

    public static void main(String[] args) {

        // Ligne
        for (int i = 1; i <= 5; i++) {
            // Etoile
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();

        }

    }

}
